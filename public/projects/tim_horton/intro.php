<p>
	Tim Horton's took over a Dunkin Donuts for a day to commemorate their
	listing on the New York Stock Exchange. I was given dimensions of various
	spaces that they were planning to occupy with banners and signs.
</p>
<p>
	Using a collection of graphics that were supplied by the client, I created
	a series of pieces which were digitally printed on vinyl and rigid boards.
</p>
