<p>
	My employer needed to create a full-page and a half-page magazine advertisement for their internet dialup service for the
	Free Methodist Church.
</p>
<p>
	They basically emailed me about 40 files in total, including many images that were too low resolution for printing,
	poor-quality logo artwork, and incomplete copy text that needed to be included.
</p>
<p>
	I cropped and resized images, redrew their logo, took screenshots of the website and created a collage out of some
	of their art and added copy. 3 revisions and 1 copy-change later, I had approval on the job and I sent it to their printer.
</p>
<p>
	The job was run again at a later date with changes.
</p>