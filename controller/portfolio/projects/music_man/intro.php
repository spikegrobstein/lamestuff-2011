<p>
	A middleschool put on a production of the Musical "The Music Man" and wanted to have tee shirts made
	for the cast and crew and to sell at the show.
</p>
<p>
	They supplied me with a copy of the cover of their score to base the art on. Since it was being printed
	as a 2-color job on black tee shirts, I had to recreate the art to work on the shirts. I opted to do it
	in vector using Illustrator. The shirts came out spectacularly.
</p>